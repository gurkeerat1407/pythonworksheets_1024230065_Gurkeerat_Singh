def move_robot(x, y, direction):
    if direction == "up":
        y += 1
    elif direction == "down":
        y -= 1
    elif direction == "left":
        x -= 1
    elif direction == "right":
        x += 1
    return (x, y)
print(move_robot(0, 0, "up"))
print(move_robot(2, 3, "left"))
print(move_robot(5, 5, "down"))
print(move_robot(1, 1, "right"))

